package br.edu.univas.vo;

public class Componente {
	
	private String nome;
	private float vlrUn;
	private int qtd;
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public float getVlrUn() {
		return vlrUn;
	}
	
	public void setVlrUn(float vlrUn) {
		this.vlrUn = vlrUn;
	}
	
	public int getQtd() {
		return qtd;
	}
	
	public void setQtd(int qtd) {
		this.qtd = qtd;
	}

}
